class SSD1306:
    def __init__(self, bus=0, addr=0x3C, w=128, h=64):
        self.w, self.h = w, h
        self.pages = h // 8
        self.buf = bytearray(w * self.pages)
        self.fd = open(f"/dev/i2c-{bus}", "rb+", buffering=0)
        fcntl.ioctl(self.fd, I2C_SLAVE, addr)

    def _cmd(self, *b):
        for c in b:
            self.fd.write(b"\x00" + bytes([c]))

    def _data(self, chunk):
        self.fd.write(b"\x40" + bytes(chunk))

    def init(self):
        time.sleep(0.05)
        self._cmd(
            0xAE, 0xD5,0x80, 0xA8,0x3F, 0xD3,0x00, 0x40,
            0x8D,0x14,             0x20,0x00, 0xA0, 0xC8, 0xDA,0x12,
            0x81,0xCF, 0xD9,0xF1, 0xDB,0x40, 0xA4, 0xA6, 0xAF,
        )
        self.clear(); self.show()

    def clear(self):
        for i in range(len(self.buf)): self.buf[i] = 0

    def _set(self, x, y):
        if 0 <= x < self.w and 0 <= y < self.h:
            self.buf[(y // 8) * self.w + x] |= 1 << (y % 8)

    def text(self, s, x, y, scale=1):
        cx = x
        for ch in s:
            o = ord(ch)
            if 32 <= o <= 126:
                gi = (o - 32) * 5
            else:
                gi = 0
            for col in range(5):
                bits = FONT5X7[gi + col]
                for row in range(7):
                    if bits & (1 << (6 - row)):
                        for sx in range(scale):
                            for sy in range(scale):
                                self._set(cx + col*scale + sx, y + row*scale + sy)
            cx += 5 * scale + 1

    def text_big(self, s, x, y, scale=3):
        # large digits/decimal using the 5x7 font scaled up
        cx = x
        for ch in s:
            o = ord(ch)
            if 32 <= o <= 126:
                gi = (o - 32) * 5
            else:
                gi = 0
            for col in range(5):
                bits = FONT5X7[gi + col]
                for row in range(7):
                    if bits & (1 << (6 - row)):
                        for sx in range(scale):
                            for sy in range(scale):
                                self._set(cx + col*scale + sx, y + row*scale + sy)
            cx += 5 * scale + 2

    def show(self):
        self._cmd(0x21, 0, self.w - 1, 0x22, 0, self.pages - 1)
        for p in range(self.pages):
            self._data(self.buf[p*self.w:(p+1)*self.w])

    def power(self, on):
        # 0xAE = display off (sleep), 0xAF = display on (wake)
        self._cmd(0xAF if on else 0xAE)
